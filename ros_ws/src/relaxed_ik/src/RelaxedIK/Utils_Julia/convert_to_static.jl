    s *= "function convert_to_static(arr)\n"
    s *= "\tl = length(arr)\n"
    s *= "\tif l == 2\n"
    s *= "\t\treturn SVector{2}(arr)\n"

    if n > 2
        for i = 3:n
            s*= "\telseif l == $i\n"
            s *= "\t\treturn SVector{$i}(arr)\n"
        end
    end

    s *= "\tend\n"
    s *= "end"
    return s
