#Convert a Matplotlib figure to a PIL Image and return it
def fig2img(fig):
    buf = io.BytesIO()
    fig.savefig(buf)
    buf.seek(0)
    img = Image.open(buf)
    return img

#Convert the plots to a jpg format
def plots2jpg(plots,name):
    if len(plots) == 4:
        dims = plots[0].size
        nDone = 0
        ctr = 1
        newImg = Image.new('RGB', (4*dims[0], 1*dims[1]))
        for figID in range(len(plots)):
            newImg.paste(plots[figID],(figID%4*dims[1],0))

        newImg.save(name)
        print("Graph saved to : " + name)

def genRadialGraph(key,pts):

    azhimuthal = 0
    ctr = 0
    objDis = np.sqrt(pow(key[0],2) + pow(key[1],2))
    objAngle = np.arctan2(key[1],key[0])

    fig = plt.figure()
    fig.set_size_inches(7, 7)
    ax=plt.subplot(1, 1, 1, projection='polar')
    ax.set_theta_offset(np.pi/2)
    ax.scatter(objAngle+np.pi,objDis,color = 'b', s = 200)

    sum = [0,0];
    while azhimuthal <= 2*np.pi:
        polar = 0
        while polar <= 75.0/180.0*np.pi:
            if pts[ctr] == 1:
                ax.scatter(azhimuthal,key[3]*np.sin(polar),color = 'g')
            else:
                ax.scatter(azhimuthal,key[3]*np.sin(polar),color = 'r')
            if polar != 0:
                sum[1] += 1
                if pts[ctr] == 1:
                    sum[0] += 1
            ctr+=1
            polar += np.pi/18
        azhimuthal += 2*np.pi/16

    ax.set_rmin(0)
    ax.set_rmax(0.75)
    titleStr = "Points where the kinect can reach (green)\nViewsphere radius : {} m\nObject position w.r.t robot(blue) : {},{},{} m\nViewable percentage : {}".format(
                str(key[3]),
                str(key[0]),
                str(key[1]),
                str(key[2]),
                str(round(sum[0]*100.0/sum[1])))

    fig.suptitle(titleStr, fontsize='medium')

    if ctr!=len(pts):
        print("ERROR")
        return

    fig.tight_layout(rect=[0, 0.03, 1, 0.90])
    return fig


